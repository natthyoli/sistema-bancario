# Conversão de tipos em Python (type casting):
# str() → converte para string (texto)
# int() → converte para inteiro (número sem vírgula)
# float() → converte para número decimal
# bool() → converte para verdadeiro/falso
# Ex.: idade = int("18"), preco = float("3.5")
preco=10.50
idade=28
print(str(preco))
print(str(idade))
texto=f"idade {idade } preco {preco }"
print(texto)

print(int(7.9))
print(float(7))
print(5//2)#divisao de dois numeros inteiros com duas barras=inteiro
print(5/2)#divisão de dois numeros inteiros com uma barra=float

